// --- Directions
// Given a string, return a new string with the reversed
// order of characters
// --- Examples
//   reverse('apple') === 'leppa'
//   reverse('hello') === 'olleh'
//   reverse('Greetings!') === '!sgniteerG'

function reverse(str) {
    // Using reverse function
    return str.split("").reverse().join("");
}

console.log(reverse("abc"));

module.exports = reverse;
